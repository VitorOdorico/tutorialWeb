/*
    Variaveis
        introdução
            - recebem valores e armazenam eles
            - atalhos para o código armazenado
            - são identificadores

        3 palavras reservadas para criação das vareáveis
            - var (pode mudar seu valor perante o ultimo valor atribuido no código)
            - let (pode mudar seu valor perante o ultimo valor atribuido no código)
            - const (não muda o valor perante um valor atribuido depois do inicial no código)


Exemplos a baixo:
*/

var clima = "um dia quente";
console.log("Hoje está ", clima)
/* 
    Oque aconteceu aqui?
        - foi criado a vareavel (clima) e ela recebeu um valor em formato de string(um dia quente).
        - logo após no console.log foi mencionado a vareavel(clima), assim imprimindo ela no console
            Resultado:
                ""Hoje está um dia quente"

        isso é oque será imprimido no console.

*/
