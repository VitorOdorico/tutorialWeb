/*
Operadores de comparação
    = atribuição de valor
    > maior que
    < menor que
    >= maior igual a
    <= menor igual a
    == igual
    === estritamente igual
    != diferente
    !== estritamente diferente
*/

let a = 5;
let b = 10;

console.log('maior que:', + a > b);
console.log('menor que:,' + a < b);
console.log('maior igual a:', + a >= b);
console.log('menor igual a:', + a <= b);
console.log('igual a:', + a == b);
console.log('estritamente igual:', + a === b);
console.log('diferente:', + a != b);
console.log('estritamente diferente:', + a !== b );